<?php
    //1~20까지 오른쪽 방향으로 찍히게 (12345...)
    for($i = 1; $i <= 20; $i++) {
        print $i;
    }

    print "<br> <br> @@@@@@@@@@@@@@@@@@@@@@@@ <br> <br>";

    //위 결과에서 $i=0 수정하지 않고 같은 결과 나오게
    for($i = 0; $i < 20; $i++) {
        print $i+1;
    }

    print "<br> <br> @@@@@@@@@@@@@@@@@@@@@@@@ <br> <br>";

    //위 결과에서 $i<50 수정하지 않고 같은 결과 나오게
    for($i = 30; $i < 50; $i++) {
        print $i-29;
    }
?>